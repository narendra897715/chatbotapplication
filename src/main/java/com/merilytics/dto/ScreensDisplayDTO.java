package com.merilytics.dto;

import java.util.ArrayList;

import org.springframework.context.annotation.Configuration;

@Configuration
public class ScreensDisplayDTO {
	private Integer mappingId;
	private Integer screenId;
	private Integer parentId;
	private String screenName;
	private String add;
	private String edit;
	private String delete;
	private ArrayList<ScreensDisplayDTO> children;

	
	
	public Integer getMappingId() {
		return mappingId;
	}

	public void setMappingId(Integer mappingId) {
		this.mappingId = mappingId;
	}

	public Integer getScreenId() {
		return screenId;
	}

	public void setScreenId(Integer screenId) {
		this.screenId = screenId;
	}

	
	
	public Integer getParentId() {
		return parentId;
	}

	public void setParentId(Integer parentId) {
		this.parentId = parentId;
	}

	public String getScreenName() {
		return screenName;
	}

	public void setScreenName(String screenName) {
		this.screenName = screenName;
	}

	public String getAdd() {
		return add;
	}

	public void setAdd(String add) {
		this.add = add;
	}

	

	public String getEdit() {
		return edit;
	}

	public void setEdit(String edit) {
		this.edit = edit;
	}

	public String getDelete() {
		return delete;
	}

	public void setDelete(String delete) {
		this.delete = delete;
	}

	public ArrayList<ScreensDisplayDTO> getChildren() {
		return children;
	}

	public void setChildren(ArrayList<ScreensDisplayDTO> children) {
		this.children = children;
	}

}
